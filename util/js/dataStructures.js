// QUEUE
//////////////////////////
let queue = [];
queue.push(); // enqueue
queue.shift(); // dequeue
!queue.length; // isEmpty
queue[0]; // peek

///////////////////////////
// STACK
//////////////////////////
let stack = [];
stack.push(); // push
stack.pop(); // pop
!stack.length; // isEmpty
stack[stack.length - 1]; // peek